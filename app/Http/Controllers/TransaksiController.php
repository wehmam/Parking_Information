    {
        $validateData = $request->validate([
            'konsumen' => 'required',
            'no_polisi' => 'required',
            'tanggal_masuk' => 'required',
            'waktu_keluar' => 'required'
        ]);
        Transaksi::create($validateData);
        return redirect()->route('transaksi.index');
    }

